# String spliceting and methods
nome = "Lucas Cardoso Barbeiro"

print(f"First five characters: {nome[0:5]}")  # First five characters
# Characters from index 6 to 13
print(f"Characters from index 6 to 13: {nome[6:14]}")
print(nome[15:])  # Characters from index 15 to the end
print(nome[:5])  # First five characters
print(nome[::2])  # Every second character
print(nome[::-1])  # Reverse the string
